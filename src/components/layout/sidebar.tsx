'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';

const navItems = [
  { href: '/', label: 'Dashboard', icon: HomeIcon },
  { href: '/record', label: 'Record', icon: MicIcon },
  { href: '/calendar', label: 'Calendar', icon: CalendarIcon },
  { href: '/meetings', label: 'Meetings', icon: FolderIcon },
  { href: '/settings', label: 'Settings', icon: GearIcon },
];

export function Sidebar() {
  const pathname = usePathname();

  return (
    <aside className="w-56 bg-card flex flex-col h-full shrink-0">
      {/* Logo area */}
      <div className="h-14 flex items-center px-5">
        <h1 className="text-sm font-semibold tracking-wide text-foreground">
          Mother&apos;s Notes
        </h1>
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-4 px-3 space-y-0.5">
        {navItems.map(({ href, label, icon: Icon }) => {
          const isActive = href === '/' ? pathname === '/' : pathname.startsWith(href);
          return (
            <Link
              key={href}
              href={href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-md text-sm transition-colors duration-100 ${
                isActive
                  ? 'bg-muted text-foreground font-medium'
                  : 'text-muted-foreground hover:text-foreground hover:bg-muted/50'
              }`}
            >
              <Icon className="w-4 h-4" />
              {label}
            </Link>
          );
        })}
      </nav>

      {/* Version */}
      <div className="px-5 py-4">
        <p className="text-xs text-muted-foreground/60">v0.1.0</p>
      </div>
    </aside>
  );
}

